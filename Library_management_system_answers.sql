-- 1. Retrieve All Books Issued by a Specific Employee 
-- Objective: Select all books issued by the employee with emp_id = 'E101'.

select b.book_tittle as Book_name
from
(select issued_book_isbn
from Issue_status
where issued_emp_id="E101")as a
join Books as b
on a.issued_book_isbn=b.isbn;

-- 2. Members Who Have Issued More Than One Book 
-- Objective: Use GROUP BY to find members who have issued more than one book.

select m.member_name,a.how_many_books_issued
from
(select issued_member_id,count(issued_id)as how_many_books_issued
from Issue_status
group by issued_member_id
having count(issued_id)>1)as a
join Members m
on m.member_id=a.issued_member_id;

