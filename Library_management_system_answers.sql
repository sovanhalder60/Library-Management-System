-- 6. List Employees with Their Branch Manager's Name and their branch details:

SELECT * from Employees;
select * from Branch;

select e.emp_id,e.emp_name,b.branch_id,b.branch_address,e.salary,ee.emp_name as manager_name
from Employees e
join Branch b
on e.branch_id=b.branch_id
join Employees ee
on ee.emp_id=b.manager_id
;



-- 7. active members,who have issued at least one book in the last 7 months.
select * from Issue_status;

select issued_member_id,count(issued_id) as total_books_issued
from Issue_status
where issued_member_id 
in(
select issued_member_id
from Issue_status
where issued_date>=date_sub(curdate(),interval 7 month))
group by issued_member_id
having count(issued_id)>=1
order by total_books_issued desc;


-- 8. Write a query to find the top 3 employees who have processed the most book issues. 
-- Display the employee name, number of books processed, and their branch.

select * from Issue_status;

select a.issued_emp_id,e.emp_name,a.total_issued_count,b.branch_address
from
(select issued_emp_id,count(issued_id)as total_issued_count
from Issue_status
group by issued_emp_id
order by total_issued_count desc
limit 3)as a
join Employees e
on a.issued_emp_id=e.emp_id
join Branch b
on e.branch_id=b.branch_id ;

